    private void lblSaveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMousePressed
        // parent component of the dialog
        if(Variables.globalXESLogListStrings.isEmpty())
        {
            MessageHelper.Warning("Nothing to save!");
            return;
        }
        
        File workingDirectory = new File(System.getProperty("user.dir"));
            try {
                workingDirectory = OpenRecently.GetWorkingDirectory();
            } catch (IOException ex) {
                System.err.println(ex.toString());
            }
        JFrame parentFrame = new JFrame();
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(workingDirectory);
        fileChooser.setDialogTitle("Specify a file to save");   
        File fileToSave = new File(Variables.logName.replace(".xes", Variables.newFileExtension));
        fileChooser.setSelectedFile(fileToSave);
        int userSelection = fileChooser.showSaveDialog(parentFrame);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            fileToSave = new File(fileChooser.getSelectedFile().getAbsoluteFile().toString());
            ThreadController.SaveFileController saveFileController = new SaveFileController(globalXESLogListStrings, fileToSave);
            Thread runSave = new Thread(saveFileController);
            runSave.run();
        }
    }//GEN-LAST:event_lblSaveMousePressed

