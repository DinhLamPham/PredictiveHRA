    private void lblViewSNAWindowsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblViewSNAWindowsMousePressed
        // TODO add your handling code here:
        if(!true)
        {
            MessageHelper.Warning("Function is under construction...");
            return;
        }
        
        if(Variables.globalGraph.getNodeCount() == 0)
        {
            MessageHelper.Warning("Nothing to analyze!");
            return;
        }
        frmSNA =  new SNAFrame();
        frmSNA.setVisible(true);
        if(SystemProperties.cbHideStartEndNode.isSelected())
            RemoveStartEndNode(globalGraph);
        
        ThreadController.CalculateGraphCentrality threadCalculate = new CalculateGraphCentrality();
        Thread thisThread = new Thread(threadCalculate);
        thisThread.start();
        
        
        
        
//        SNAHelper.FormatGraph.SetDefaultStyle(Variables.globalGraph);
        
        try {
            FormatGraph.FormatNodeOnDegree(Variables.globalGraph, "Degree");
        } catch (InterruptedException ex) {
            Logger.getLogger(LeftPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

                
    }//GEN-LAST:event_lblViewSNAWindowsMousePressed

    public static void RemoveStartEndNode(Graph graph)
    {
        graph.removeNode("START");
        graph.removeNode("END");
    }
    private void lblOpenrecentlyMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblOpenrecentlyMousePressed
        // TODO add your handling code here:
        File workingDirectory = new File(System.getProperty("user.dir"));
        
        try {
            workingDirectory = OpenRecently.GetWorkingDirectory();
            
            LeftPanelHelper.rawFileName = workingDirectory.getAbsolutePath();
            Variables.logName = workingDirectory.getName();

            ThreadController.ReadFileController readFileController = new ReadFileController();
            Thread threadReadFile = new Thread(readFileController);
            threadReadFile.start();
            
            
        } catch (IOException ex) {
            System.err.println(ex.toString());
        }
    }//GEN-LAST:event_lblOpenrecentlyMousePressed

